#' @title Simple function that calculates the sum
#' @description This function takes two argument (`a` and `b`) and calculates the sum of both.
#' @param a summand (numeric)
#' @param b summand (numeric)
#' @examples
#' \dontrun{make_sum(2, 3)}

make_sum <- function(a, b) {
  c <- a + b
  return(c)
}
